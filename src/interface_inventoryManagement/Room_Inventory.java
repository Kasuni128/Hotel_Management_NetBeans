    private void jButtonUpdateRoomInventoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateRoomInventoryActionPerformed

        int RItemId = 0;
        String RItemName = jTextFieldRItemName.getText();
        //String RQuanitity = jTextFieldRQuanitity.getText();
        //String Rsupplier = jTextFieldRSupplierName.getText();
        String Rcategory = jComboBoxRCategory.getSelectedItem().toString();

        java.util.Date date = jDateChooserRDate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateR = oDateFormat.format(date);

        //int quantity = Integer.valueOf(jTextFieldRQuanitity.getText());

        try {
            RItemId = Integer.valueOf(jLabelRItemId.getText());

            if (roominventorydetails.updateroominventroies(RItemId, RItemName, Rcategory, dateR)) {
                JOptionPane.showMessageDialog(rootPane, "Update Room Inventory  Successfully", "Update Room Inventory", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, " Room Inventory not Update", "Update not Room Inventory", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the RItem Id", "Room inventory Id Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonUpdateRoomInventoryActionPerformed

    private void jButtonAddRoomInventoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddRoomInventoryActionPerformed
        String RItemName = jTextFieldRItemName.getText();
        //String RQuanitity = jTextFieldRQuanitity.getText();
       // String Rsupplier = jTextFieldRSupplierName.getText();
        String Rcategory = String.valueOf(jComboBoxRCategory.getSelectedItem().toString());

        java.util.Date date = jDateChooserRDate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateR = oDateFormat.format(date);

        

        if(RItemName.isEmpty() ){
            JOptionPane.showMessageDialog(rootPane, "Required Fileds", "Fill Empty Fileds", JOptionPane.WARNING_MESSAGE);
        }
        else{
            if (roominventorydetails.addroominventroies(RItemName, Rcategory, dateR)) {
            JOptionPane.showMessageDialog(rootPane, "New Room Inventory Add Successfully", "Add Room Inventory", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Room Inventory not Add", "Add not Room Inventory", JOptionPane.INFORMATION_MESSAGE);
        }
        }
        
        jTextFieldRItemName.setText("");
        //jTextFieldRQuanitity.setText("");
        //jTextFieldRSupplierName.setText("");
        jComboBoxRCategory.setSelectedItem("");

    }//GEN-LAST:event_jButtonAddRoomInventoryActionPerformed

