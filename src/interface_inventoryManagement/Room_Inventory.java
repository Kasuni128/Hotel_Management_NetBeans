    private void jTableRoomInventoiesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableRoomInventoiesMouseClicked
        DefaultTableModel model = (DefaultTableModel) jTableRoomInventoies.getModel();

        //get the selected row index
        int rIndex = jTableRoomInventoies.getSelectedRow();

        Date date = null;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String) jTableRoomInventoies.getValueAt(rIndex, 3));

        } catch (ParseException ex) {
            Logger.getLogger(Room_Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }

        //String month = jTableEmpAttendance.getValueAt(rIndex, 3).toString();

        String category = jTableRoomInventoies.getValueAt(rIndex, 2).toString();
        //display data
        jLabelRItemId.setText(model.getValueAt(rIndex, 0).toString());
        jTextFieldRItemName.setText(model.getValueAt(rIndex, 1).toString());
        //jTextFieldRQuanitity.setText(model.getValueAt(rIndex, 2).toString());
        jComboBoxRCategory.setSelectedItem(category);
        jDateChooserRDate.setDate(date);

    }//GEN-LAST:event_jTableRoomInventoiesMouseClicked

