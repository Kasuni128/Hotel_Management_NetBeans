    private void jButtonAddKitchenInventoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddKitchenInventoryActionPerformed
        String KItemName = jTextFieldKItemName.getText();
        String Kcategory = String.valueOf(jComboBoxKCategory.getSelectedItem().toString());
        
        java.util.Date date = jDateChooserKDate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateK = oDateFormat.format(date); 
        
        
        
         if(KItemName.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Required Fileds", "Fill Empty Fileds", JOptionPane.WARNING_MESSAGE);
        }
         else{
        if (kitcheninventorydetails.addkitcheninventroies(KItemName, Kcategory, dateK)) {
            JOptionPane.showMessageDialog(rootPane, "New Kitchen Inventory Add Successfully", "Add Kitchen Inventory", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Kitchen Inventory not Add", "Add not Kitchen", JOptionPane.INFORMATION_MESSAGE);
        }
         }
        jTextFieldKItemName.setText("");
        jComboBoxKCategory.setSelectedItem("");
    }//GEN-LAST:event_jButtonAddKitchenInventoryActionPerformed
    
    private void jButtonUpdateKitchenInventoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateKitchenInventoryActionPerformed
        
         int KItemId = 0;
        String KItemName = jTextFieldKItemName.getText();
        String Kcategory = jComboBoxKCategory.getSelectedItem().toString();
       
        java.util.Date date = jDateChooserKDate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateK = oDateFormat.format(date); 
        
        
        try {
            KItemId = Integer.valueOf(jLabelKItemId.getText());

            if (kitcheninventorydetails.updatekitcheninventroies(KItemId, KItemName, Kcategory, dateK)) {
                JOptionPane.showMessageDialog(rootPane, "Update Kitchen Inventory  Successfully", "Update Kitchen Inventory", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, " Kitchen Inventory not Update", "Update not Kitchen Inventory", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the KItem Id", "Kitchen inventory Id Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonUpdateKitchenInventoryActionPerformed

