    private void jTableKitchenInventoiesMouseClicked(java.awt.event.MouseEvent evt) {
         DefaultTableModel model = (DefaultTableModel) jTableKitchenInventoies.getModel();

        //get the selected row index
        int rIndex = jTableKitchenInventoies.getSelectedRow();
        
        Date date = null;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String) jTableKitchenInventoies.getValueAt(rIndex, 3));
           
        } catch (ParseException ex) {
            Logger.getLogger(Room_Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         //String month = jTableEmpAttendance.getValueAt(rIndex, 3).toString();
        
        
        String category = jTableKitchenInventoies.getValueAt(rIndex, 2).toString();
        //display data
        jLabelKItemId.setText(model.getValueAt(rIndex, 0).toString());
        jTextFieldKItemName.setText(model.getValueAt(rIndex, 1).toString());
        jComboBoxKCategory.setSelectedItem(category);
        jDateChooserKDate.setDate(date);
        
        
    }  
    
       
