    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          String path="";
        JFileChooser j=new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x=j.showSaveDialog(this);
        
        if(x==JFileChooser.APPROVE_OPTION){
            path=j.getSelectedFile().getPath();
        }
        
        Document doc = new Document();
         
         try {
             PdfWriter.getInstance(doc, new FileOutputStream(path+"Avaliable_Kitchen_Inventory_Daily_Report.pdf"));
             
             doc.open();
              doc.add(new Paragraph("Avaliable Kitchen Inventory Report"));
             PdfPTable tb1=new PdfPTable(2);
             tb1.addCell("Avaliable Kitchen Inventory Product Name");
             tb1.addCell("Total Weigt(Kg)");
             
             
             for (int i=0 ; i <jTableKitchen.getRowCount();i++){
             
                   String ProductName = jTableKitchen.getValueAt(i,0).toString();
                   String Qty = jTableKitchen.getValueAt(i,1).toString();
                   
                   tb1.addCell(ProductName);
                   tb1.addCell(Qty);         
                       
             }
             doc.add(tb1);
         } catch (FileNotFoundException ex) {
             Logger.getLogger(Kitchen_Inventory.class.getName()).log(Level.SEVERE, null, ex);
         } catch (DocumentException ex) {
             Logger.getLogger(Kitchen_Inventory.class.getName()).log(Level.SEVERE, null, ex);
         }
        doc.close();
   
    }                                        

