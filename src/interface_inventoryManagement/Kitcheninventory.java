    private void jButtonAddNewProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddNewProductActionPerformed
        catregoryItem citem=(catregoryItem)jComboBoxKitchen.getSelectedItem();
        String qty = jTextFieldweight.getText();
        //String status = String.valueOf(jComboBoxstatus.getSelectedItem().toString());
        int Qty = Integer.parseInt(qty);

        if(citem.name.isEmpty()|| qty.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Required Fileds", "Fill Empty Fileds", JOptionPane.WARNING_MESSAGE);
        }
        else{
            if (ins.addproduct(citem.name, Qty)) {
                JOptionPane.showMessageDialog(rootPane, "New Kitchen Inventory qty Add Successfully", "Add Kitchen Inventory", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Kitchen Inventory qty not Add", "Add not Kitchen", JOptionPane.INFORMATION_MESSAGE);
            }

            jComboBoxKitchen.setSelectedIndex(-1);
            jTextFieldweight.setText("");
    }//GEN-LAST:event_jButtonAddNewProductActionPerformed
    }
    private void jButtonIncreaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncreaseActionPerformed
        catregoryItem citem=(catregoryItem)jComboBoxKitchen.getSelectedItem();
        //String product=(String)jComboBox1.getSelectedItem();
        String qty=jTextFieldweight.getText();
        int Qty=Integer.parseInt(qty);
        //int Productid=Integer.parseInt(productid);

        try {

            if (ins.updateproduct(citem.name, Qty)) {
                JOptionPane.showMessageDialog(rootPane, "Add Kitchen Inventory Product qty Successfully", "Add Kitchen Inventory Product aty", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, " Kitchen inventory products qty not add before. Click Add New product Button and Try again", "Add not Kitchen Inventory Product qty", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the KItem Id", "Kitchen inventory Id Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonIncreaseActionPerformed

