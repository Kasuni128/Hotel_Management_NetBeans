     private void Kitchen_Inventory(){
        PreparedStatement ps;
        ResultSet rs;
         MY_CONNECTION my_connection = new MY_CONNECTION();
        String selectQuery = "SELECT * FROM `kitcheninventory`";
        
        try {
            ps = my_connection.createConnection().prepareStatement(selectQuery);
            
            rs = ps.executeQuery();
            
          
            jComboBoxKitchen.removeAllItems();
            
            
            while(rs.next())
            {
              jComboBoxKitchen.addItem(new catregoryItem(rs.getInt(1),rs.getString(2)));
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(KitchenInventoryDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
     }
     

