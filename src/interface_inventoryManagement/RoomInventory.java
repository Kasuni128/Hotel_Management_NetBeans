     private void Room_Inventory(){
        PreparedStatement ps;
        ResultSet rs;
        
        MY_CONNECTION my_connection = new MY_CONNECTION();
        String selectQuery = "SELECT * FROM `roominventory`";
        
        try {
            ps = my_connection.createConnection().prepareStatement(selectQuery);
            
            rs = ps.executeQuery();
          
            jComboBoxroom.removeAllItems();
            
            
            while(rs.next())
            {
              jComboBoxroom.addItem(new catregoryRoomItem(rs.getInt(1),rs.getString(2)));
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(RoomInventoryDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }
