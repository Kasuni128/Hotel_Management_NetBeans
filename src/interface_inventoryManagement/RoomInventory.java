    private void jButtonnewproductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonnewproductActionPerformed
        catregoryRoomItem citem=(catregoryRoomItem)jComboBoxroom.getSelectedItem();
        String qty = jTextFieldqty.getText();
        //String status = String.valueOf(jComboBoxstatus.getSelectedItem().toString());
        int Qty = Integer.parseInt(qty);

        if(citem.name.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Required Fileds", "Fill Empty Fileds", JOptionPane.WARNING_MESSAGE);
        }
        else{
            if (room.addproduct(citem.name, Qty)) {
                JOptionPane.showMessageDialog(rootPane, "New Room Inventory Product Add Successfully", "Add Room Inventory Product Qty", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Room Inventory Product not Add", "Add not Room Product Qty", JOptionPane.INFORMATION_MESSAGE);
            }

            jComboBoxroom.setSelectedIndex(-1);
            jTextFieldqty.setText("");
    }//GEN-LAST:event_jButtonnewproductActionPerformed
    }
    private void jButtonincreaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonincreaseActionPerformed
        catregoryRoomItem citem=(catregoryRoomItem)jComboBoxroom.getSelectedItem();
        //String product=(String)jComboBox1.getSelectedItem();
        String qty=jTextFieldqty.getText();
        int Qty=Integer.parseInt(qty);
        //int Productid=Integer.parseInt(productid);

        try {

            if (room.updateproduct(citem.name, Qty)) {
                JOptionPane.showMessageDialog(rootPane, "Add Room Inventory Product qty Successfully", "Add Room Inventory Product qty", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, " Room Inventory prodcut qty not add before. Click Add New product Button and Try again", "Add not Room Inventory Product qty", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the KItem Id", "Room inventory Id Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonincreaseActionPerformed

