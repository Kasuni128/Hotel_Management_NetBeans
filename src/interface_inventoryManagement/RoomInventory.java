    private void jButtonprintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonprintActionPerformed
  
        String path="";
        JFileChooser j=new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x=j.showSaveDialog(this);
        
        if(x==JFileChooser.APPROVE_OPTION){
            path=j.getSelectedFile().getPath();
        }
        
        Document doc = new Document();
        
         try {
             
             PdfWriter.getInstance(doc, new FileOutputStream(path+"Avaliable_Room_Inventory_Daily_Report.pdf"));
             
             doc.open();
            doc.add(new Paragraph("Avaliable Room Inventory Report"));
            
            
             PdfPTable tb1=new PdfPTable(2);
           
             tb1.addCell("Avaliable Room Item Name");
             tb1.addCell("Total Qunantity");
             
             
             for (int i=0 ; i <jTableroom.getRowCount();i++){
             
                   String ProductName = jTableroom.getValueAt(i,0).toString();
                   String Qty = jTableroom.getValueAt(i,1).toString();
                  
                   tb1.addCell(ProductName);
                   tb1.addCell(Qty);         
                       
             }
             doc.add(tb1);
         } catch (FileNotFoundException ex) {
             Logger.getLogger(Kitchen_Inventory.class.getName()).log(Level.SEVERE, null, ex);
         } catch (DocumentException ex) {
             Logger.getLogger(Kitchen_Inventory.class.getName()).log(Level.SEVERE, null, ex);
         }
        doc.close();
    }//GEN-LAST:event_jButtonprintActionPerformed

