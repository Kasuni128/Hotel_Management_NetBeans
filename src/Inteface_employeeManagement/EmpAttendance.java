    private void attAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_attAddBtnActionPerformed
        // TODO add your handling code here:
        
        String empId = jTextFieldEmpID.getText();
        //String date =   jDateChooserAttDate.getDateFormatString();
        java.util.Date date = jDateChooserAttDate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date1 = oDateFormat.format(date); 
       
        
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
	Date dateCheck = new Date();
	String checkDate = (formatter.format(dateCheck));
         
        String otHours = jTextFieldOTHours.getText();
        
        Calendar mCalendar = Calendar.getInstance();    
        String month = mCalendar.getDisplayName(Calendar.MONTH, Calendar.LONG, Locale.getDefault());
        
        if(empId.isEmpty() || otHours.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Required Fields ", "Fill Empty Fields", JOptionPane.WARNING_MESSAGE);
        }
        else if(checkDate.equals(date1)){
            //java.sql.Date date = new java.sql.Date(jDateChooserAttDate.getDate().getTime());
        if (empAttdetails.addEmployerAttendance(empId, date1,month, otHours)) {
            JOptionPane.showMessageDialog(rootPane, "New Employer Attendance Add Successfully", "Add Employer Attendace", JOptionPane.INFORMATION_MESSAGE);
            
            jTextFieldEmpID.setText("");
            jDateChooserAttDate.setDate(null);
            jTextFieldOTHours.setText("");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Employer Attendance not Add", "Add not Employer Attendance", JOptionPane.INFORMATION_MESSAGE);
        }
        }
        else{
           JOptionPane.showMessageDialog(rootPane, "Enter the current Date", "Date Error", JOptionPane.ERROR_MESSAGE);
           jDateChooserAttDate.setDate(null);
        }
        
        
        
    }//GEN-LAST:event_attAddBtnActionPerformed

    private void jTableEmpAttendanceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEmpAttendanceMouseClicked
        // TODO add your handling code here:
        
        //get the jTableEmployeeAttendanceDetails model
        DefaultTableModel model = (DefaultTableModel) jTableEmpAttendance.getModel();

        //get the selected row index
        int rIndex = jTableEmpAttendance.getSelectedRow();
        
        jTableEmpAttendance.setSelectionBackground(Color.WHITE);
        jTableEmpAttendance.setSelectionForeground(Color.BLACK);

        //display data
        Date date = null;
        try {
            date =   new SimpleDateFormat("yyyy-MM-dd").parse((String) jTableEmpAttendance.getValueAt(rIndex, 2));

        } catch (ParseException ex) {
            Logger.getLogger(EmpAttendance.class.getName()).log(Level.SEVERE, null, ex);
        }
        String month = jTableEmpAttendance.getValueAt(rIndex, 3).toString();
        
        jTextFieldAttID.setText(model.getValueAt(rIndex, 0).toString());
        jTextFieldEmpID.setText(model.getValueAt(rIndex, 1).toString());
        jDateChooserAttDate.setDate(date);
       // jComboBoxMonth.setSelectedItem(month);
        jTextFieldOTHours.setText(model.getValueAt(rIndex, 4).toString());
    }//GEN-LAST:event_jTableEmpAttendanceMouseClicked

