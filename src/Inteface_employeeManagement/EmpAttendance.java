    private void attUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_attUpdateBtnActionPerformed
        // TODO add your handling code here:
        
        int attId = 0;
        int empId = Integer.valueOf(jTextFieldEmpID.getText());
        
        java.util.Date date = jDateChooserAttDate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date1 = oDateFormat.format(date); 
        
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
	Date dateCheck = new Date();
	String checkDate = (formatter.format(dateCheck));
        
       
        Calendar mCalendar = Calendar.getInstance();    
        String month = mCalendar.getDisplayName(Calendar.MONTH, Calendar.LONG, Locale.getDefault());
        
        int otHours = Integer.valueOf(jTextFieldOTHours.getText());
        
       try {
            attId = Integer.valueOf(jTextFieldAttID.getText());
            
            if(checkDate.equals(date1)){
                if (empAttdetails.updateEmployerAttendance(attId, empId, date1, month, otHours)) {
                JOptionPane.showMessageDialog(rootPane, "Update Employer Attendance Successfully", "Update Attendance Employer details", JOptionPane.INFORMATION_MESSAGE);
                
                jTextFieldAttID.setText("");
                jTextFieldEmpID.setText("");
                jDateChooserAttDate.setDate(null);
                jTextFieldOTHours.setText("");
        
            } else {
                JOptionPane.showMessageDialog(rootPane, " Employer Attendance not Update", "Update not Employer Attendance", JOptionPane.INFORMATION_MESSAGE);
            }
           }
            else{
                JOptionPane.showMessageDialog(rootPane, "Enter the current Date", "Date Error", JOptionPane.ERROR_MESSAGE);
                jDateChooserAttDate.setDate(null);
            }

            
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the Employer Attendance Id", "Attendance Id Error", JOptionPane.ERROR_MESSAGE);
        }
       
        
    }//GEN-LAST:event_attUpdateBtnActionPerformed

