    private void jTableutilityMouseClicked(java.awt.event.MouseEvent evt) {                                                  
        DefaultTableModel model = (DefaultTableModel) jTableutility.getModel();

        //get the selected row index
        int rIndex = jTableutility.getSelectedRow();

        Date date = null;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String) jTableutility.getValueAt(rIndex, 4));

        } catch (ParseException ex) {
            Logger.getLogger(UtilityBills.class.getName()).log(Level.SEVERE, null, ex);
        }

        //String month = jTableEmpAttendance.getValueAt(rIndex, 3).toString();

        String category = jTableutility.getValueAt(rIndex, 4).toString();
        //display data
        jTextFieldID.setText(model.getValueAt(rIndex, 0).toString());
        jComboBoxCategory.setSelectedItem(category);
        jTextFieldamount.setText(model.getValueAt(rIndex, 2).toString());
        jTextFieldBillName.setText(model.getValueAt(rIndex, 3).toString());
        
        jDateChooserRDate.setDate(date);

    }
    
    
