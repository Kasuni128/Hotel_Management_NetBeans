    private void jButton8ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ADDActionPerformed

        String category = String.valueOf(jComboBoxCategory.getSelectedItem().toString());
        String amount = jTextFieldamount.getText();
        String billname = jTextFieldBillName.getText();

        java.util.Date date = jDateChooserRDate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateR = oDateFormat.format(date);

        if(amount.isEmpty() || billname.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Required Fileds", "Fill Empty Fileds", JOptionPane.WARNING_MESSAGE);
        }
        else{
            if (financedetails.addutility(category, amount, billname,  dateR)) {
                JOptionPane.showMessageDialog(rootPane, "Utility Add Successfully", "Add Utility", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Utility not Add", "Add not Utility", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        jComboBoxCategory.setSelectedItem("");
        jTextFieldamount.setText("");
        jTextFieldBillName.setText("");

    }//GEN-LAST:event_jButton8ADDActionPerformed

    private void jButton9UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9UpdateActionPerformed

        int Id = 0;
        String Rcategory = jComboBoxCategory.getSelectedItem().toString();
        //String RQuanitity = jTextFieldRQuanitity.getText();
        String Amount = jTextFieldamount.getText();
        String Billname = jTextFieldBillName.getText();

        java.util.Date date = jDateChooserRDate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateR = oDateFormat.format(date);

        try {
            Id = Integer.valueOf(jTextFieldID.getText());

            if (financedetails.updateutility(Id, Rcategory, Amount, Billname, dateR)) {
                JOptionPane.showMessageDialog(rootPane, "Update Utility Bill Successfully", "Update Utility Bill", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, " Utility Bill not Update", "Update not Utility Bill", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the Id", "Utility Bill Id Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton9UpdateActionPerformed

