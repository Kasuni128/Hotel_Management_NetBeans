    private void jButtonupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonupdateActionPerformed
        int supplierId = 0;
        String SupplierName = jTextFieldsuppliername.getText();
        //String RQuanitity = jTextFieldRQuanitity.getText();
        String CompanyName =  jTextFieldcname.getText();
        String Address = jTextFieldaddress.getText();
        
        String Type = jComboBoxType.getSelectedItem().toString();

        

       
        int contactno = Integer.valueOf(jTextFieldcontactno.getText());

        try {
            supplierId = Integer.valueOf(jTextFieldsupplierid.getText());

            if (supplierdetails.updatesupplierdetails(supplierId, SupplierName, CompanyName, Address, contactno, Type)) {
                JOptionPane.showMessageDialog(rootPane, "Update Supplier Successfully", "Update Supplier Details", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, " Supplier  not Update", "Update supplier Inventory", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the RItem Id", "Room inventory Id Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonupdateActionPerformed

