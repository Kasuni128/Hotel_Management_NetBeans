    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
         String SupplierName = jTextFieldsuppliername.getText();
        String CompanyName = jTextFieldcname.getText();
        String Address = jTextFieldaddress.getText();
        String ContactNo = jTextFieldcontactno.getText();
        String Type = String.valueOf(jComboBoxType.getSelectedItem().toString());

       Pattern p = Pattern.compile("^[0][0-9]{9}$");
        Matcher m = p.matcher(jTextFieldcontactno.getText());
        
        
        Integer contactno = Integer.parseInt(ContactNo);

        
          if(SupplierName.isEmpty() || CompanyName.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Required Fileds", "Fill Empty Fileds", JOptionPane.WARNING_MESSAGE);
        }
          else{
              if(m.matches()){
        JOptionPane.showMessageDialog(null, "Valid mobile Number");
              
        if (supplierdetails.addsupplierdetails(SupplierName, CompanyName, Address, contactno, Type)) {
            JOptionPane.showMessageDialog(rootPane, "New Supplier Add Successfully", "Add Supplier", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "New Supplier not Add", "Add not Supplier", JOptionPane.INFORMATION_MESSAGE);
        }
          }
              else{
         JOptionPane.showMessageDialog(null, "Invalid mobile Number");
         
        }
          }
              
        jTextFieldsuppliername.setText("");
        jTextFieldcname.setText("");
        jTextFieldaddress.setText("");
        jTextFieldcontactno.setText("");
        jComboBoxType.setSelectedItem("");
    }//GEN-LAST:event_jButton11ActionPerformed

