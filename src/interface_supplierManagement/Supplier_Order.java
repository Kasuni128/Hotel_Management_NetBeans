    private void jButtonaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonaddActionPerformed
       String SupplierId = jTextFieldsupplierid.getText();
        String ProductId = jTextFieldproductid.getText();
        String ProductName = jTextFieldproductname.getText();
        String Qty = jTextFieldqty.getText();
        String Unit = String.valueOf(jComboBox1.getSelectedItem().toString());

        java.util.Date date = jDateChooserdate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateR = oDateFormat.format(date);

        Integer supplierid = Integer.parseInt(SupplierId);
        Integer productid = Integer.parseInt(ProductId);
        Integer qty = Integer.parseInt(Qty);

        
         if(ProductName.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Required Fileds", "Fill Empty Fileds", JOptionPane.WARNING_MESSAGE);
        }
         else{
        if (orderdetails.addorderdetails(supplierid, productid, ProductName, dateR, qty,Unit)) {
            JOptionPane.showMessageDialog(rootPane, "New Order Add Successfully", "Add Order", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Order not Add", "Add not Order", JOptionPane.INFORMATION_MESSAGE);
        }
         }
        jTextFieldsupplierid.setText("");
        jTextFieldproductid.setText("");
        jTextFieldproductname.setText("");
        jTextFieldqty.setText("");
        jComboBox1.setSelectedItem("");
    }//GEN-LAST:event_jButtonaddActionPerformed

    private void jButtonupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonupdateActionPerformed
    int Num = 0;
        String SupplierId = jTextFieldsupplierid.getText();
        String ProductId = jTextFieldproductid.getText();
        String ProductName = jTextFieldproductname.getText();
        String Qty = jTextFieldqty.getText();
        String Unit = jComboBox1.getSelectedItem().toString();

        java.util.Date date = jDateChooserdate.getDate();
        SimpleDateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateR = oDateFormat.format(date);

        Integer supplierid = Integer.parseInt(SupplierId);
        Integer productid = Integer.parseInt(ProductId);
        Integer qty = Integer.parseInt(Qty);


        try {
            Num = Integer.valueOf(jTextFieldnumber.getText());

            if (orderdetails.updateorderdetails(Num, supplierid, productid, ProductName, dateR, qty,Unit)) {
                JOptionPane.showMessageDialog(rootPane, "Update Order  Successfully", "Update Order", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, " Order not Update", "Update not Order", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the Num", "Order Id Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonupdateActionPerformed

