    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        int roomPrice = 0;
        String id = txtId.getText();
        String roomNo = txtRoomNo.getText();
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        //get date values as String for database use
        String strInDate = sdf.format(jDateChooser1.getDate());
        String strOutDate = sdf.format(jDateChooser2.getDate());
        
        String sqlPrice = "SELECT * FROM booking WHERE room_no='"+roomNo+"'";
        String sqlUpdate = "UPDATE checkout SET room_no=?,in_date=?,out_date=?,total_price=? WHERE id='"+id+"'";
     //get date values from defaul date format to difference calculation
            Date inDate = jDateChooser1.getDate();
            Date outDate = jDateChooser2.getDate();
            
            long diffInMillies = Math.abs(outDate.getTime()-inDate.getTime());
            long diff =TimeUnit.DAYS.convert(diffInMillies, TimeUnit.MILLISECONDS);
        
            try {
                pst4 = con.prepareStatement(sqlPrice);
                rs4 = pst4.executeQuery();
                while(rs4.next()){
                    roomPrice = rs4.getInt("room_price");
                }
            } catch (SQLException ex) {
                Logger.getLogger(UpdateBooking.class.getName()).log(Level.SEVERE, null, ex);
            }  
            tcost = roomPrice * Integer.valueOf(String.valueOf(diff));
            try {
                pst5=con.prepareStatement(sqlUpdate);
                //pst5.setString(1,null);
                pst5.setString(1,roomNo);
                pst5.setString(2,strInDate);
                pst5.setString(3,strOutDate);
                pst5.setInt(4,tcost);
                pst5.executeUpdate();
                JOptionPane.showMessageDialog(null, "Updated Successfully...!");
            } catch (Exception ex) {
                ex.printStackTrace();
            }                
    }//GEN-LAST:event_jButtonAddActionPerformed

