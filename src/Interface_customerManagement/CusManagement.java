    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
            String cusName = JTextFieldCusName.getText();
            String nic = JTextFieldNic.getText();
            String email = JTextFieldEmail.getText();
            String address = JTextFieldAddress.getText();
            String contactNo = JTextFieldContactNo.getText();
            
            Integer contactno = Integer.parseInt(contactNo);            
            
            
        if(cusName.isEmpty() || nic.isEmpty() || email.isEmpty() || address.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Required Fileds", "Fill Empty Fileds", JOptionPane.WARNING_MESSAGE);
        }
        else{
            if(customerdetails.addcustomerdetails(cusName, nic, email, address, contactno)){
                 JOptionPane.showMessageDialog(rootPane, "Customer Add Successfully","Add Customer", JOptionPane.INFORMATION_MESSAGE);
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Customer Not Add Successfully", "Add Not Customer",JOptionPane.INFORMATION_MESSAGE);
                
            }
        }
        JTextFieldCusName.setText("");
        JTextFieldNic.setText("");
        JTextFieldEmail.setText("");
        JTextFieldAddress.setText("");
        JTextFieldContactNo.setText("");
        
        
         

            
    }//GEN-LAST:event_btnADDActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      
        int customerId = 0;
            String cusName = JTextFieldCusName.getText();
            String nic = JTextFieldNic.getText();
            String email = JTextFieldEmail.getText();
            String address = JTextFieldAddress.getText();
            String contactNo = JTextFieldContactNo.getText();
       
             Integer contactno = Integer.parseInt(contactNo); 
        try {
            customerId = Integer.valueOf(JTextFieldcusID.getText());

            if (customerdetails.updateCustomer(customerId, cusName, nic, email, address, contactno)) {
                JOptionPane.showMessageDialog(rootPane, "Update Cutomer  Successfully", "Update Customer", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, " Customer not Update", "Update not Customer", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, ne.getMessage() + "Enter the Customer Id", "Customer Id Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       int customerId = Integer.valueOf(JTextFieldcusID.getText());

       
        if (customerdetails.deleteCustomer(customerId)) {
            JOptionPane.showMessageDialog(rootPane, "Customer details deleted successfully", "Delete Customer", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Customer Details Not deleted", "Delete Customer Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

